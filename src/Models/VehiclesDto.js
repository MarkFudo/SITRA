class VehiculoDto {
    constructor(id, marca, modelo, placa, color, tipo, capacidad, status, kilometraje) {
        this.id = id;
        this.marca = marca;
        this.modelo = modelo;
        this.placa = placa;
        this.color = color;
        this.tipo = tipo;
        this.capacidad = capacidad;
        this.status = status;
        this.kilometraje = kilometraje;
    }
}
